//Operadores Racionais

//Os operadores mais comuns sÃ£o:

/*
Igual a ==
Diferente de !=
Maior que > 
Menor que <
Maior ou igual >= 
Menor ou igual <=

Igual e do mesmo tipo que ===
Diferente e de outro tipo que !==
*/

var x = 5, y = 8

console.log(x == y);// Igual a ==
console.log(x != y);// Diferente de !=
console.log(x > y);// Maior que > 
console.log(x < y);// Menor que <
console.log(x >= y);// Maior ou igual >= 
console.log(x <= y);// Menor ou igual <=
console.log(x === y);// Igual e do mesmo tipo que ===
console.log(x = y);// Diferente e de outro tipo que !==
