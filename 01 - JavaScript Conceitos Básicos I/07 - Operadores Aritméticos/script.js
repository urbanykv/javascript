console.log(Math.pow(3,2)); // 9 (Elevado)

console.log(Math.sqrt(81)); // 9 (Raiz Quadrada)

console.log(typeof(42)); // Number
console.log(typeof('Giulia')); // String

console.log(Number.isInteger(45)); // True
console.log(Number.isInteger('Giulia')); // False

